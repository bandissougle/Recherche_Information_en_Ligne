    private void buttonAssocierInfoBibliotekeurActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAssocierInfoBibliotekeurActionPerformed

        //new AID("Vendeurs 1", AID.ISLOCALNAME).

        // TODO add your handling code here:
        String nomAgent = comboxListBibliotekeur.getSelectedItem().toString();
        String info_recherche = comboxListInformations.getSelectedItem().toString();
        String prix = textPrix.getText();


        if (!nomAgent.isEmpty() && !info_recherche.isEmpty() && !prix.isEmpty()) {
            GuiEvent gev = new GuiEvent(this, 2);

            Map<String, Object> params = new HashMap<>();
            params.put("AGENT", nomAgent);
            params.put("INFORMATION", info_recherche);
            params.put("PRIX", prix);

            //Ajout du paramètres
            gev.addParameter(params);

            //Renvoie des paramètres à l'agent consommateur par le biais de la methode onGuiEvent()
            getAgentsUtilisateur().onGuiEvent(gev);
            System.out.println(" ajout info fait avec succès ");
            textPrix.setText("");
        }


    }//GEN-LAST:event_buttonAssocierInfoBibliotekeurActionPerformed

