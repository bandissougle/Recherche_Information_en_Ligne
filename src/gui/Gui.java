    private void buttonEnvoyerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonEnvoyerActionPerformed

        String agent = comboAgentsRechercheurs.getSelectedItem().toString();
        String info_recherche = comboxListesInfosRecherches.getSelectedItem().toString();
        displayMessage("Demande de l'information : " + info_recherche + " prise en charge par l'agent : " + comboAgentsRechercheurs.getSelectedItem().toString());

        if (!agent.isEmpty() && !info_recherche.isEmpty()) {
            GuiEvent gev = new GuiEvent(this, 1);

            Map<String, Object> params = new HashMap<>();
            params.put("AGENT", agent);
            params.put("INFORMATION", info_recherche);

            //Ajout du paramètres
            gev.addParameter(params);

            //Renvoie des paramètres à l'agent consommateur par le biais de la methode onGuiEvent()
            getAgentsUtilisateur().onGuiEvent(gev);
        }
    }//GEN-LAST:event_buttonEnvoyerActionPerformed

